package com.company;

public class Utilities {
    public int dogYears(int realYears){
        return realYears * 7;
    }

    public int fishFibber(int realLength) {
        return realLength + 5;
    }

    public double averageAge(int age1, int age2, int age3) {
        return (age1+age2+age3) / 3;
    }
}
